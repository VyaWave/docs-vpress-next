// @lc code=start
/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var findKthLargest = function (nums, k) {
  const array = nums.sort((a, b) => b - a).filter((_, i) => i == k - 1);

  return array[0];
};
